function doMaths(num1, num2) {
  console.log(`
    ${num1} + ${num2} = ${num1 + num2}
    ${num1} - ${num2} = ${num1 - num2}
    ${num1} * ${num2} = ${num1 * num2}
    ${num1} / ${num2} = ${num1 / num2}
    ${num1} % ${num2} = ${num1 % num2}
    ${num1} ** ${num2} = ${num1 ** num2}
  `);
}

doMaths(23, 17);
